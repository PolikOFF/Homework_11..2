def returns_capital_letters(word: str):
    """Функция возвращает все буквы слова заглавными буквами"""
    upper_word = word.upper()
    return upper_word


def return_title_all_words(word: str):
    """Функция возвращает каждое слово строки с заглавной буквы"""
    capital_word = word.title()
    return capital_word
